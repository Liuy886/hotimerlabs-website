document.querySelector("[data-lead-form]").addEventListener("submit", async (event) => {
  event.preventDefault();
  const formData = new FormData(event.currentTarget);
  const message = [
    "Hi HotimerLabs, I would like to discuss Web3 consulting support.",
    `Project: ${formData.get("project") || "N/A"}`,
    `Stage: ${formData.get("stage") || "N/A"}`,
    `Need: ${formData.get("need") || "N/A"}`
  ].join("\n");
  const telegramUrl = `https://t.me/${telegramHandle}`;
  const telegramWindow = window.open("", "_blank");

  if (telegramWindow) {
    telegramWindow.opener = null;
  }

  try {
    await navigator.clipboard.writeText(message);
  } catch (error) {
    window.prompt("Copy this message to Telegram:", message);
  }

  if (telegramWindow) {
    telegramWindow.location.replace(telegramUrl);
    return;
  }

  window.location.href = telegramUrl;
});

