const motionTimers = new WeakMap();
const motionScreens = Array.from(document.querySelectorAll("main > section"));

document.body.classList.add("js-motion");

const clearMotionTimer = (screen) => {
  const timer = motionTimers.get(screen);

  if (timer) {
    window.clearTimeout(timer);
    motionTimers.delete(screen);
  }
};

const resetScreen = (screen) => {
  clearMotionTimer(screen);
  screen.classList.remove("is-entering", "is-active");
  screen.classList.add("is-ready");
};

const enterScreen = (screen) => {
  clearMotionTimer(screen);
  screen.classList.remove("is-ready", "is-active");
  void screen.offsetWidth;
  screen.classList.add("is-entering");

  const timer = window.setTimeout(() => {
    screen.classList.remove("is-entering");
    screen.classList.add("is-active");
    motionTimers.delete(screen);
  }, 1120);

  motionTimers.set(screen, timer);
};

motionScreens.forEach((screen) => {
  screen.classList.add("motion-screen");
  resetScreen(screen);
});

const screenObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      const screen = entry.target;
      const ratio = entry.intersectionRatio;

      if (entry.isIntersecting && ratio >= 0.2) {
        if (!screen.classList.contains("is-entering") && !screen.classList.contains("is-active")) {
          enterScreen(screen);
        }
        return;
      }

      if (!entry.isIntersecting || ratio <= 0.08) {
        resetScreen(screen);
      }
    });
  },
  {
    threshold: [0, 0.08, 0.16, 0.24, 0.42],
    rootMargin: "-6% 0px -10% 0px"
  }
);

motionScreens.forEach((screen) => {
  screenObserver.observe(screen);
});

